export function categoryFallback(category) {
  if (category === 'serif') return 'serif';
  if (category === 'mono') return 'monospace';
  if (category === 'display') return 'sans-serif';
  return 'sans-serif';
}

// Build a font-family stack string suitable for a CSS style attribute.
export function buildStack(family, category) {
  return `'${family}', ${categoryFallback(category)}`;
}
