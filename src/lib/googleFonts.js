// Lazy-load a Google Font family at runtime by injecting a <link> into <head>.
// De-duplicates: each family is loaded at most once per page session.

const loaded = new Set();

const familyToUrl = (family, weights = '400;500;600;700') =>
  `https://fonts.googleapis.com/css2?family=${family.replace(/\s+/g, '+')}:wght@${weights}&display=swap`;

export function ensureGoogleFont(family) {
  if (!family || loaded.has(family)) return;
  loaded.add(family);
  const link = document.createElement('link');
  link.rel = 'stylesheet';
  link.href = familyToUrl(family);
  link.dataset.googleFont = family;
  document.head.appendChild(link);
}

// Bulk: ensure many families are loaded (used by the picker for live previews).
export function ensureGoogleFonts(families) {
  families.forEach(ensureGoogleFont);
}

